package org.sunbird.logging;

/**
 * Enum representing the various logging levels supported by the application.
 * content:
 * - INFO
 * - WARN
 * - DEBUG
 * - ERROR
 * - BE_LOG (Backend Log)
 * - PERF_LOG (Performance Log)
 */
public enum LoggerEnum {
  INFO,
  WARN,
  DEBUG,
  ERROR,
  BE_LOG,
  PERF_LOG;
}
