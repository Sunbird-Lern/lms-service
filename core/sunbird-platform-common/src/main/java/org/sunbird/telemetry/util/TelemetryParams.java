package org.sunbird.telemetry.util;

/**
 * Enum to represent standard Telemetry parameters.
 */
public enum TelemetryParams {
  /**
   * Represents the channel ID.
   */
  CHANNEL,
  
  /**
   * Represents the environment ID.
   */
  ENV,
  
  /**
   * Represents the actor information.
   */
  ACTOR;
}
